// Track used images to ensure no duplicates
export class ImageManager {
  constructor() {
    this.usedImages = new Set();
    this.imageCounters = {};
  }

  getImageForProduct(category, keywords) {
    // Normalize category
    const normalizedCategory = category || "Electronics";

    // Find matching keyword from product name
    const matchedKeyword = this.findMatchingKeyword(
      normalizedCategory,
      keywords
    );

    if (!matchedKeyword) {
      return this.getDefaultImage();
    }

    // Initialize counter for this category-keyword combo
    const key = `${normalizedCategory}-${matchedKeyword}`;
    if (!this.imageCounters[key]) {
      this.imageCounters[key] = 0;
    }

    // Get the image array
    const imageArray = PRODUCT_IMAGES[normalizedCategory]?.[matchedKeyword];
    if (!imageArray || imageArray.length === 0) {
      return this.getDefaultImage();
    }

    // Get next unused image (cycle through if all used)
    const index = this.imageCounters[key] % imageArray.length;
    const image = imageArray[index];

    // Increment counter
    this.imageCounters[key]++;

    return image;
  }

  findMatchingKeyword(category, productName) {
    if (!productName) return null;

    const lowercaseName = productName.toLowerCase();
    const categoryImages = PRODUCT_IMAGES[category];

    if (!categoryImages) return null;

    // Find first keyword that matches product name
    for (const keyword of Object.keys(categoryImages)) {
      if (lowercaseName.includes(keyword)) {
        return keyword;
      }
    }

    // Return first keyword as default
    return Object.keys(categoryImages)[0];
  }

  getDefaultImage() {
    return "https://images.unsplash.com/photo-1505740420928-5e560c06d30e?w=300&h=300&fit=crop";
  }

  reset() {
    this.usedImages.clear();
    this.imageCounters = {};
  }
}

// Global image manager instance
export const imageManager = new ImageManager();
